CODE_TO_INDUSTRY = {}

for industry, (score, codes) in AI_KNOWLEDGE_BASE.items():
    for code in codes:
        if code not in CODE_TO_INDUSTRY:
            CODE_TO_INDUSTRY[code] = {'industries': [], 'max_score': 0}
        if industry not in CODE_TO_INDUSTRY[code]['industries']:
            CODE_TO_INDUSTRY[code]['industries'].append(industry)
        CODE_TO_INDUSTRY[code]['max_score'] = max(CODE_TO_INDUSTRY[code]['max_score'], score)


def match_by_name_traditional(name: str) -> tuple:
    """传统行业名称关键词匹配，返回 (行业列表, 热度分)"""
    for industry, heat, keywords, _ in TRADITIONAL_INDUSTRIES:
        for kw in keywords:
            if kw in name:
                return [industry], heat
    return [], 0


def lookup_knowledge(code: str, name: str = None) -> tuple:
    """查询知识库（AI赛道精确 + 手动品牌 + 传统行业名称）"""
    info = CODE_TO_INDUSTRY.get(code)
    if info:
        return info['industries'][:3], info['max_score']
    
    manual = MANUAL_TRADITIONAL.get(code)
    if manual:
        return [manual[0]], manual[1]
    
    if name:
        industries, score = match_by_name_traditional(name)
        if industries:
            return industries, score
    
    return [], 0


def get_industry_score(code: str, name: str = None) -> float:
    _, score = lookup_knowledge(code, name)
    return score


def stat_knowledge_base():
    total_codes = len(CODE_TO_INDUSTRY)
    print(f"AI赛道精确覆盖: {total_codes} 只, {len(AI_KNOWLEDGE_BASE)} 个赛道")
    for industry, (score, codes) in sorted(AI_KNOWLEDGE_BASE.items(), key=lambda x: -x[1][0]):
        print(f"  {industry} (热度{score}): {len(codes)} 只")
    print(f"\n传统行业匹配: {len(TRADITIONAL_INDUSTRIES)} 个赛道（按名称关键词）")
    for industry, heat, keywords, _ in TRADITIONAL_INDUSTRIES:
        print(f"  {industry} (热度{heat}): {len(keywords)}个关键词")


def coverage_stat(whitelist_path: str = "stock_whitelist.json"):
    """计算实际覆盖率"""
    with open(whitelist_path) as f:
        whitelist = json.load(f)
    
    ai_matched = 0
    manual_matched = 0
    trad_matched = 0
    unmatched = 0
    
    for s in whitelist:
        code = s['code']
        name = s['name']
        if code in CODE_TO_INDUSTRY:
            ai_matched += 1
        elif code in MANUAL_TRADITIONAL:
            manual_matched += 1
        else:
            inds, score = match_by_name_traditional(name)
            if inds:
                trad_matched += 1
            else:
                unmatched += 1
    
    total = len(whitelist)
    covered = ai_matched + manual_matched + trad_matched
    print(f"\n覆盖率统计:")
    print(f"  AI赛道精确: {ai_matched} ({ai_matched/total*100:.1f}%)")
    print(f"  手动品牌:   {manual_matched} ({manual_matched/total*100:.1f}%)")
    print(f"  传统关键词: {trad_matched} ({trad_matched/total*100:.1f}%)")
    print(f"  总覆盖: {covered} ({covered/total*100:.1f}%)")
    print(f"  未覆盖: {unmatched} ({unmatched/total*100:.1f}%)")
    return covered


