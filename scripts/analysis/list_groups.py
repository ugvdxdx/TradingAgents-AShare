def classify(name, code):
    for kw, ind in IND_KEYWORDS:
        if kw in name:
            return ind
    if code.startswith('688'): return 'AI芯片'
    if code.startswith('30'): return '创业板'
    return '工业制造'

groups = defaultdict(list)
for code, name, mcap in uncovered:
    groups[classify(name, code)].append((code, name, mcap))

for ind, stocks in sorted(groups.items(), key=lambda x: -len(x[1])):
    print(f"\n{'='*60}")
    print(f"[{ind}] {len(stocks)} stocks")
    for code, name, mcap in stocks:
        print(f"  {code} {name} (mcap={mcap}yi)")