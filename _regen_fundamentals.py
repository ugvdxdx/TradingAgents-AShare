def _calc_growth_score(metrics: dict, industry: str) -> float:
    """计算增长评分"""
    score = 5.0
    roe = metrics.get("roe_pct") or 0
    nm = metrics.get("net_margin_pct") or 0
    rd = metrics.get("rd_ratio_pct") or 0

    # 高景气行业加分
    hot = ["半导体", "通信设备", "AI芯片", "光模块", "锂电池", "储能", "新型电力"]
    if industry in hot:
        score += 3
    elif industry in ["银行", "煤炭开采", "水泥", "钢铁", "房地产"]:
        score -= 2

    if roe >= 15:
        score += 1
    elif roe < 5:
        score -= 1

    if rd and rd >= 10:
        score += 1

    if nm and nm < 0:
        score -= 2

    return max(0, min(10, score))


def main():
    # 加载股票集合
    with open("/tmp/final_stock_set.json") as f:
        stock_set = json.load(f)

    print(f"共 {len(stock_set)} 只股票需要生成")

    # 清空fundamentals
    fund_dir = os.path.join(os.path.dirname(os.path.abspath(__file__)), "fundamentals")
    if os.path.exists(fund_dir):
        for f in os.listdir(fund_dir):
            if f.endswith(".json"):
                os.remove(os.path.join(fund_dir, f))
    os.makedirs(fund_dir, exist_ok=True)
    print("已清空fundamentals目录")

    success = 0
    fail = 0
    no_data = 0

    for i, stock in enumerate(stock_set):
        code = stock["code"]
        name = stock.get("name", "")
        industry = stock.get("industry", "")
        total_mv = stock.get("total_mv", 0)

        if (i + 1) % 50 == 0:
            print(f"进度: {i+1}/{len(stock_set)} (成功{success} 失败{fail} 无数据{no_data})")

        try:
            result = generate_fundamental(code, name, industry, total_mv)

            # 检查是否有实质内容
            has_fin = result["financial_health"]["key_metrics"].get("revenue_yi") is not None
            has_wk = len(result["business_overview"]["what_they_do"]) > 20

            if not has_fin and not has_wk:
                no_data += 1
                continue

            path = os.path.join(fund_dir, f"{code}.json")
            with open(path, "w", encoding="utf-8") as f:
                json.dump(result, f, ensure_ascii=False, indent=2)
            success += 1

            # 限速：避免请求过快
            if (i + 1) % 10 == 0:
                time.sleep(1)

        except Exception as e:
            print(f"  错误 {code} {name}: {e}")
            fail += 1

    print(f"\n完成！成功{success} 失败{fail} 无数据{no_data}")


if __name__ == "__main__":
    main()
