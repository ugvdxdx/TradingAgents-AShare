def normalize_sector(raw: str) -> Optional[str]:
    """归一化 sector 标签到标准赛道名。

    Returns:
        标准赛道名 (str), 或 None 表示应跳过 (太泛/非行业)。
        未命中映射的标签保留原值 (不强行归入, 避免误聚合)。
    """
    if not raw:
        return None
    s = str(raw).strip()
    if not s:
        return None
    if s in _SKIP_SECTORS:
        return None
    return _SECTOR_MAP.get(s, s)


