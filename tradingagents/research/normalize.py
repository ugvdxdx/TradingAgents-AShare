def get_sector_keyword_index() -> dict:
    """生成 {标准赛道: [匹配关键词列表]} 的反向索引。

    从 _SECTOR_MAP 的所有 key (碎片标签) + _SECTOR_EXTRA_KEYWORDS (细分概念)
    合并去重生成, 作为 _INDUSTRY_KEYWORD_MAP 的单一真相源。
    供 consumer.get_industry_research_brief() 做个股 industry → 板块关键词匹配。
    """
    # 标准 → 碎片 key 列表
    sector_to_keys: dict = {}
    for raw_key, std_sector in _SECTOR_MAP.items():
        sector_to_keys.setdefault(std_sector, []).append(raw_key)
    # 合并额外关键词
    result = {}
    all_sectors = set(sector_to_keys.keys()) | set(_SECTOR_EXTRA_KEYWORDS.keys())
    for sector in all_sectors:
        kws = set()
        kws.update(sector_to_keys.get(sector, []))
        kws.update(_SECTOR_EXTRA_KEYWORDS.get(sector, []))
        # 过滤太短 (<2字) 和标准赛道名自身
        kws = {k for k in kws if len(k) >= 2}
        if kws:
            result[sector] = sorted(kws)
    return result
