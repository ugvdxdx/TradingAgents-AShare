# 允许落库的 info_type 白名单 (与 cleaner.InfoType 对齐)
ALLOWED_INFO_TYPES = frozenset({
    'pre_market',   # 盘前
    'intraday',     # 盘中
    'post_market',  # 盘后复盘
    'research',     # 研报
    'announcement', # 公告
    'general',      # 通用
})

# LLM 实际产出的碎片标签 → 标准类型映射
# (实测入库 14 种, 其中 8 种不在白名单内)
_INFO_TYPE_MAP = {
    # 盘前
    'pre_market': 'pre_market', 'premarket': 'pre_market',
    'before_hours': 'pre_market', 'morning': 'pre_market',
    'morning_review': 'pre_market',
    # 盘中
    'intraday': 'intraday', 'midday': 'intraday', 'noon_review': 'intraday',
    # 盘后
    'post_market': 'post_market', 'after_hours': 'post_market',
    'close': 'post_market', 'close_review': 'post_market',
    # 研报
    'research': 'research', 'analysis': 'research',
    # 公告/通用 (默认)
    'announcement': 'announcement', 'general': 'general',
}


def normalize_info_type(raw: str) -> str:
    """归一化 info_type 到白名单。未知值归为 general。"""
    if not raw:
        return 'general'
    key = str(raw).strip().lower()
    return _INFO_TYPE_MAP.get(key, 'general')


# ═══════════════════════════════════════════════════════════
# 2. sentiment 归一化
# ═══════════════════════════════════════════════════════════

# 允许落库的 sentiment 白名单
ALLOWED_SENTIMENTS = frozenset({'bullish', 'bearish', 'neutral'})

# LLM 产出过 bullish/bearish/neutral/positive/negative 五种,
# 后两者是脏数据, 统一映射掉。
_SENTIMENT_MAP = {
    'bullish': 'bullish', 'positive': 'bullish',
    'bearish': 'bearish', 'negative': 'bearish',
    'neutral': 'neutral',
}


def normalize_sentiment(raw: str) -> str:
    """归一化 sentiment 到 {bullish,bearish,neutral}。未知值归 neutral。"""
    if not raw:
        return 'neutral'
    key = str(raw).strip().lower()
    return _SENTIMENT_MAP.get(key, 'neutral')


